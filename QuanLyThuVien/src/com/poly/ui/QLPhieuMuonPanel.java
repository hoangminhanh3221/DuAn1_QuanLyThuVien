    private void update() {
        PhieuMuon pm = new PhieuMuon(
                txtPhieuMuon.getText(),
                cboMaSV.getSelectedItem().toString(),
                cboMaNV.getSelectedItem().toString(),
                XDate.toString(txtNgayMuon.getDate(), "dd/MM/yyyy"),
                XDate.toString(txtNgayTra.getDate(), "dd/MM/yyyy"),
                txtGhiChu.getText(),
                false
        );
        try {
            pmDAO.update(pm);
            MsgBox.alert(this, "Sua thanh cong!");
            fillToTable();
        } catch (Exception e) {
            MsgBox.alert(this, "Sua that bai!");
        }
    }

    private void delete() {
        try {
            pmDAO.delete(txtPhieuMuon.getText());
            MsgBox.alert(this, "Xoa thanh cong!");
            fillToTable();
        } catch (Exception e) {
            MsgBox.alert(this, "Xoa that bai!");
        }
    }

    private void clearForm() {
        txtPhieuMuon.setText("");
        cboMaSV.setSelectedIndex(0);
        cboMaNV.setSelectedIndex(0);
        setNgayMuon();
        modelCTPM.setRowCount(0);
    }

    private void setNgayMuon() {
        Calendar cal = Calendar.getInstance(); // Lấy thời gian hiện tại
        txtNgayMuon.setDate(cal.getTime()); // Set ngày tháng năm cho dateChooser
        cal.add(Calendar.DAY_OF_MONTH, 7); // Thêm 7 ngày
        txtNgayTra.setDate(cal.getTime());
    }

    private void first() {
        tblPhieuMuon.setRowSelectionInterval(0, 0);
        onAndOff(false, true, true, true);
    }

    private void previous() {
        onAndOff(true, true, true, true);
        int max = modelPM.getRowCount();
        int index = tblPhieuMuon.getSelectedRow() - 1;
        if (index < 0) {
            tblPhieuMuon.setRowSelectionInterval(max - 1, max - 1);
            btnLast.setEnabled(false);
        } else {
            tblPhieuMuon.setRowSelectionInterval(index, index);
            if (index == 0) {
                btnFirst.setEnabled(false);
            }
        }
    }

    private void next() {
        onAndOff(true, true, true, true);
        int max = modelPM.getRowCount();
        int index = tblPhieuMuon.getSelectedRow() + 1;
        if (index == max) {
            tblPhieuMuon.setRowSelectionInterval(0, 0);
            btnFirst.setEnabled(false);
        } else {
            tblPhieuMuon.setRowSelectionInterval(index, index);
            if (index == max - 1) {
                btnLast.setEnabled(false);
            }
        }
    }

    private void last() {
        int max = modelPM.getRowCount();
        tblPhieuMuon.setRowSelectionInterval(max - 1, max - 1);
        onAndOff(true, true, true, false);
    }

    private void onAndOff(boolean... b) {
        btnFirst.setEnabled(b[0]);
        btnPre.setEnabled(b[1]);
        btnNext.setEnabled(b[2]);
        btnLast.setEnabled(b[3]);
    }

    private void timKiem() {
        String sql = "select * from PhieuMuon where MaPhieuMuon like '%" + txtTimKiem.getText() + "%'";
        listPM = pmDAO.selectBySql(sql);
        if (!listPM.isEmpty()) {
            modelPM.setRowCount(0);
            int stt = 1;
            for (PhieuMuon pm : listPM) {
                modelPM.addRow(new Object[]{
                    stt, pm.getMaPhieuMuon(), pm.getMaSinhVien(), pm.getMaNhanVien(), pm.getNgayMuon(), pm.getNgayTra(), pm.getNgayTra(), pm.isTrangThai(), pm.getGhiChu()
                });
                stt++;
            }
            tblPhieuMuon.setRowSelectionInterval(0, 0);
            btnFirst.setEnabled(false);
        }
    }


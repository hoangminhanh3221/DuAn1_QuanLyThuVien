    DefaultTableModel modelPM, modelCTPM;
    PhieuMuonDAO pmDAO = new PhieuMuonDAO();
    List<PhieuMuon> listPM = new ArrayList<>();
    List<ChiTietPhieuMuon> listCTPM = new ArrayList<>();

    private void setTable() {
        modelPM = new DefaultTableModel();
        String[] colums = new String[]{
            "STT", "Ma phieu muon", "Ma sinh vien", "Ma nhan vien", "Ngay muon", "Ngay tra", "Trang thai", "Ghi chu"
        };
        modelPM.setColumnIdentifiers(colums);
        tblPhieuMuon.setModel(modelPM);
        
        modelCTPM = new DefaultTableModel();
        String[] columsCTPM = new String[]{
            "STT", "Ma phieu muon", "Ma sach", "So luong"
        };
        modelCTPM.setColumnIdentifiers(columsCTPM);
        tblCTPM.setModel(modelCTPM);
    }

    private void fillToTable() {
        listPM = pmDAO.selectAll();
        modelPM.setRowCount(0);
        int stt = 1;
        for (PhieuMuon pm : listPM) {
            modelPM.addRow(new Object[]{
                stt, pm.getMaPhieuMuon(), pm.getMaSinhVien(), pm.getMaNhanVien(), pm.getNgayMuon(), pm.getNgayTra(), pm.getNgayTra(), pm.isTrangThai(), pm.getGhiChu()
            });
            stt++;
        }
        tblPhieuMuon.setRowSelectionInterval(0, 0);
        btnFirst.setEnabled(false);
    }
    private void fillToTableCTHD(String maPM) {
        listCTPM = pmDAO.selectCTPM(maPM);
        modelCTPM.setRowCount(0);
        int stt = 1;
        for (ChiTietPhieuMuon ctpm : listCTPM) {
            modelCTPM.addRow(new Object[]{
                stt, ctpm.getMaPhieuMuon(), ctpm.getMaSach(), ctpm.getSoLuong()
            });
            stt++;
        }
        btnFirst.setEnabled(false);
    }

    private void loadForm(int index) {
        PhieuMuon pm = listPM.get(index);
        txtPhieuMuon.setText(pm.getMaPhieuMuon());
        cboMaSV.setSelectedItem(pm.getMaSinhVien());
        cboMaNV.setSelectedItem(pm.getMaNhanVien());
        txtNgayMuon.setDate(XDate.toDate(pm.getNgayMuon(), "dd/MM/yyyy"));
        txtNgayTra.setDate(XDate.toDate(pm.getNgayTra(), "dd/MM/yyyy"));
        if(pm.isTrangThai()){
            rdoChuaTra.setSelected(true);
        } else {
            rdoDaTra.setSelected(true);
        }
        txtGhiChu.setText(pm.getGhiChu());
        fillToTableCTHD(pm.getMaPhieuMuon());
    }

    private void setMaSV() {
        cboMaSV.removeAllItems();
        cboMaSV.addItem("Chon sinh vien");
        for (String masv : pmDAO.getMaSinhVien()) {
            cboMaSV.addItem(masv);
        }
    }

    private void setMaNV() {
        cboMaNV.removeAllItems();
        cboMaNV.addItem("Chon nhan vien");
        for (String manv : pmDAO.getMaNhanVien()) {
            cboMaNV.addItem(manv);
        }
    }

    private boolean isCheckForm() {
        if (txtPhieuMuon.getText().equals("")) {
            txtPhieuMuon.requestFocus();
            MsgBox.alert(this, "Chưa nhập mã phieu muon");
            return false;
        } else if (cboMaNV.getSelectedIndex() == 0 || cboMaNV.getSelectedIndex() == -1) {
            MsgBox.alert(this, "Chua chon ma the loai!");
            return false;
        } else if (cboMaNV.getSelectedIndex() == 0 || cboMaNV.getSelectedIndex() == -1) {
            MsgBox.alert(this, "Chua chon ma tac gia!");
            return false;
        } else if (txtNgayTra.getDate() == null) {
            MsgBox.alert(this, "Chua chon ngay tra sach");
            return false;
        } else if (txtNgayMuon.getDate() == null) {
            MsgBox.alert(this, "Chua chon ngay muon sach");
            return false;
        } else if (pmDAO.checkMaPhieuMuon(txtPhieuMuon.getText())) {
            MsgBox.alert(this, "Ma phieu muon da ton tai!");
            return false;
        } else {
            return true;
        }
    }

    private boolean isCheckFormUpdate() {
        if (txtPhieuMuon.getText().equals("")) {
            txtPhieuMuon.requestFocus();
            MsgBox.alert(this, "Chưa nhập mã phieu muon");
            return false;
        } else if (cboMaNV.getSelectedIndex() == 0 || cboMaNV.getSelectedIndex() == -1) {
            MsgBox.alert(this, "Chua chon ma the loai!");
            return false;
        } else if (cboMaNV.getSelectedIndex() == 0 || cboMaNV.getSelectedIndex() == -1) {
            MsgBox.alert(this, "Chua chon ma tac gia!");
            return false;
        } else if (txtNgayTra.getDate() == null) {
            MsgBox.alert(this, "Chua chon ngay tra sach");
            return false;
        } else if (txtNgayMuon.getDate() == null) {
            MsgBox.alert(this, "Chua chon ngay muon sach");
            return false;
        } else if (!pmDAO.checkMaPhieuMuon(txtPhieuMuon.getText())) {
            MsgBox.alert(this, "Ma phieu muon khong ton tai!");
            return false;
        } else {
            return true;
        }
    }


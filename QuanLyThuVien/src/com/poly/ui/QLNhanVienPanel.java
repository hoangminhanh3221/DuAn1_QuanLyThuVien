    private void fillTable() {

        // Lấy model của table 
        DefaultTableModel modelTT = (DefaultTableModel) tblNhanVien.getModel();

        // Xóa tất cả các hàng trên table
        modelTT.setRowCount(0);

        // Load dữ liệu vào table
        try {
            List<NhanVien> listTT = daoTG.selectAll();
            for (NhanVien cd : listTT) {
                Object[] row1 = {cd.getMaNhanVien(), cd.getMatKhau() , cd.getTenNhanVien(), cd.getNgaySinh(), cd.isGioiTinh(), cd.getDiaChi(), cd.getSoDienThoai(), cd.getEmail()};
                modelTT.addRow(row1);
                
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }
    
    //Các nút chuyển index
    
    void first() {
        rowNV = 0;
        edit();
    }

    void prev() {
        if (rowNV > 0) {
            rowNV--;
            edit(); 
        }
    }

    void next() {
        if (rowNV < tblNhanVien.getRowCount() - 1){
            rowNV++;
            edit(); 
        }
    }

    void last() {
        rowNV = tblNhanVien.getRowCount() - 1;
        edit();
    }


    NhanVienDAO daoTG = new NhanVienDAO();
    int rowNV = -1;
    
    private NhanVien getFormTG() {
        NhanVien cd = new NhanVien();
        cd.setMaNhanVien(txtManhanvien.getText());
        cd.setTenNhanVien(txtTennhanvien.getText());
        cd.setDiaChi(txtDiachi.getText());
        cd.setEmail(txtEmail.getText());
        cd.setHinhAnh("Hinh"+txtManhanvien.getText()+".jpg");
        cd.setMatKhau(txtPassword.getText());
        cd.setSoDienThoai(txtSDT.getText());
        cd.setNgaySinh(XDate.toString(dateNgaySinh.getDate(), "dd/MM/yyyy"));
        if(rdoNam.isSelected())
            cd.setGioiTinh(true);
        else 
            cd.setGioiTinh(false);
        return cd;
    }
    
    private void insert(){
        NhanVien cd = getFormTG(); // lấy thông tin nhập trên getForm đưa vào biến cd
        try {
            daoTG.insert(cd);
            fillTable();
            this.resetForm();
            MsgBox.alert(this,"Thêm mới thành công!");
        } catch (Exception e) {
            e.printStackTrace();
          MsgBox.alert(this,"Thêm mới thất bại!");
        }
    }
    
    private void update(){
        NhanVien cd = getFormTG(); // lấy thông tin nhập trên getForm đưa vào biến cd
        try {
            daoTG.update(cd);
            fillTable();
            MsgBox.alert(this,"Cập nhật thành công!");
        } catch (Exception e) {
            e.printStackTrace();
          MsgBox.alert(this,"Cập nhật thất bại!");
        }
    }
    
    private void edit() {
        String maTG = (String) tblNhanVien.getValueAt(this.rowNV, 0);
        NhanVien tt = daoTG.selectById(maTG);
        
        txtManhanvien.setEnabled(false);
        txtManhanvien.setEditable(false);
        
        btnF.setEnabled(true);
        btnP.setEnabled(true);
        btnN.setEnabled(true);
        btnL.setEnabled(true);
        
        this.setFormTT(tt);
        btnThem.setEnabled(false);
        btnSua.setEnabled(true);
        btnXoa.setEnabled(true);
    }
    
    private boolean checkNull(){
        if(txtManhanvien.getText().equals("") || txtTennhanvien.getText().equals("") || dateNgaySinh.getDate().equals("") ||
                txtDiachi.getText().equals("") || txtEmail.getText().equals("") || txtPassword.getText().equals("") || txtSDT.getText().equals("")){
            MsgBox.alert(this,"Vui lòng nhập đầy đủ thông tin!");
            return false;
        }else{
            return true;
        }
    }
    
    private void delete(){
            boolean a = MsgBox.confirm(this, "Bạn thực sự muốn xóa người này ?");
            if(a){
                String maTT = txtManhanvien.getText();
                try {
                    daoTG.delete(maTT);
                    this.fillTable();
                    this.resetForm();
                    this.rowNV = -1;
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    e.printStackTrace();
                    MsgBox.alert(this,"Xóa thất bại!");
                }
            }
     }
    
    private void setFormTT(NhanVien cd) {
        
        txtManhanvien.setText(cd.getMaNhanVien());
        txtTennhanvien.setText(cd.getTenNhanVien());
        txtDiachi.setText(cd.getDiaChi());
        txtEmail.setText(cd.getEmail());
        txtPassword.setText(cd.getMatKhau());
        txtSDT.setText(cd.getSoDienThoai());
        dateNgaySinh.setDate(XDate.toDate(cd.getNgaySinh(), "dd/MM/yyyy"));
        lblhinh.setText(cd.getHinhAnh());
        if(cd.isGioiTinh()) rdoNam.setSelected(true);
        else rdoNu.setSelected(true);
            
        btnThem.setEnabled(true);
        btnSua.setEnabled(false);
        btnXoa.setEnabled(false);
    }
    
   private void findNhanVienByID(String id){
       if(id.length() == 0){
           fillTable();
       }else{
           try{
                NhanVien cd = daoTG.selectById(id);
                
                // Lấy model của table 
                DefaultTableModel modelTT = (DefaultTableModel) tblNhanVien.getModel();

                // Xóa tất cả các hàng trên table
                modelTT.setRowCount(0);

                Object[] row1 = {cd.getMaNhanVien(), cd.getMatKhau() , cd.getTenNhanVien(), cd.getNgaySinh(), cd.isGioiTinh(), cd.getDiaChi(), cd.getSoDienThoai(), cd.getEmail()};
                modelTT.addRow(row1);
           }
           catch(Exception e){
               MsgBox.alert(this,"Mã tác giả không tồn tại!");
           }
       
       }
   }
    
